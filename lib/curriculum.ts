export const LEVEL_NAMES: Record<number, string> = Object.fromEntries(
  MODULE_CURRICULUM.map((module) => [module.level_number, module.title])
);

export const LEVEL_GOALS: Record<number, string> = Object.fromEntries(
  MODULE_CURRICULUM.map((module) => [module.level_number, module.goal])
);

/** Section display names by "level.section". */
export const SECTION_NAMES_BY_LEVEL: Record<string, string> = Object.fromEntries(
  MODULE_CURRICULUM.map((module) => [`${module.level_number}.1`, `${module.title} Lessons`])
);

export function getSectionName(level: number, section: number): string {
  return SECTION_NAMES_BY_LEVEL[`${level}.${section}`] ?? `Section ${section}`;
}

export function parseTopicId(topicId: string): { level: number; section: number; lesson: number } | null {
  const parts = topicId.split(".").map(Number);
  if (parts.length !== 3 || parts.some(isNaN)) return null;
  return { level: parts[0], section: parts[1], lesson: parts[2] };
}

/** Section-relative lesson counts per (level, section) for `getNextTopicId`. */
const LESSON_COUNTS: { level: number; section: number; count: number }[] = MODULE_CURRICULUM.map((module) => ({
  level: module.level_number,
  section: 1,
  count: module.lessons.length,
}));

/** First lesson topic id from coded curriculum, or null if none (unlock logic uses DB order). */
export function getFirstTopicId(): string | null {
  const first = LESSON_COUNTS[0];
  if (!first) return null;
  return `${first.level}.${first.section}.1`;
}

/** Topic IDs on the learn path (lessons + checkpoints + boss). Empty = restructure mode (trust DB only). */
export const VALID_LEARN_TOPIC_IDS: Set<string> = (() => {
  const set = new Set<string>();
  for (const r of LESSON_COUNTS) {
    for (let i = 1; i <= r.count; i++) set.add(`${r.level}.${r.section}.${i}`);
  }
  return set;
})();

/** When the coded curriculum is empty, show all topics from the database. Otherwise filter out stale rows. */
export function isKnownCurriculumTopic(topicId: string): boolean {
  if (VALID_LEARN_TOPIC_IDS.size === 0) return true;
  return VALID_LEARN_TOPIC_IDS.has(topicId);
}

export function getNextTopicId(topicId: string): string | null {
  const p = parseTopicId(topicId);
  if (!p) return null;
  const { level, section, lesson } = p;
  const rangeIndex = LESSON_COUNTS.findIndex((r) => r.level === level && r.section === section);
  if (rangeIndex < 0) return null;
  const range = LESSON_COUNTS[rangeIndex];
  if (lesson < range.count) return `${level}.${section}.${lesson + 1}`;
  const nextRange = LESSON_COUNTS[rangeIndex + 1];
  if (!nextRange) return null;
  return `${nextRange.level}.${nextRange.section}.1`;
}

/** Human-readable titles (topic_id → title). */
export const LESSON_TITLES: Record<string, string> = Object.fromEntries(
  MODULE_CURRICULUM.flatMap((module) =>
    module.lessons.map((lesson) => [`${module.level_number}.1.${lesson.lesson_number}`, lesson.title])
  )
);

/** Curriculum-native descriptions keyed by topic id. */
export const CURRICULUM_LESSON_DESCRIPTIONS: Record<string, string> = Object.fromEntries(
  MODULE_CURRICULUM.flatMap((module) =>
    module.lessons.map((lesson) => [`${module.level_number}.1.${lesson.lesson_number}`, lesson.description])
  )
);

export function getLessonTitle(topicId: string): string {
  const p = parseTopicId(topicId);
  return LESSON_TITLES[topicId] ?? (p ? `Lesson ${p.lesson}` : topicId);
}

const CHECKPOINT_DESCRIPTION =
  "Test your understanding of this section with a short quiz. Pass with 80% or higher to unlock the next section.";

export function getLessonDescription(topicId: string): string | null {
  if (topicId.endsWith(".checkpoint")) return CHECKPOINT_DESCRIPTION;
  return LESSON_DESCRIPTIONS[topicId] ?? CURRICULUM_LESSON_DESCRIPTIONS[topicId] ?? null;
}

const LESSON_RANGES: { level: number; section: number; lessonStart: number; lessonEnd: number }[] = MODULE_CURRICULUM.map((module) => ({
  level: module.level_number,
  section: 1,
  lessonStart: 1,
  lessonEnd: module.lessons.length,
}));

export interface LessonTopic {
  topic_id: string;
  level_number: number;
  section_number: number;
  lesson_number: number;
  levelName: string;
  sectionName: string;
  title: string;
  description: string | null;
}

export function getAllLessonTopics(): LessonTopic[] {
  const out: LessonTopic[] = [];
  for (const r of LESSON_RANGES) {
    const count = r.lessonEnd - r.lessonStart + 1;
    for (let i = 1; i <= count; i++) {
      const topic_id = `${r.level}.${r.section}.${i}`;
      out.push({
        topic_id,
        level_number: r.level,
        section_number: r.section,
        lesson_number: i,
        levelName: LEVEL_NAMES[r.level] ?? `Level ${r.level}`,
        sectionName: getSectionName(r.level, r.section),
        title: getLessonTitle(topic_id),
        description: getLessonDescription(topic_id),
      });
    }
  }
  return out;
}

